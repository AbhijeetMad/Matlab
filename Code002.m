num1 = input('Enter the first number: ');
num2 = input('Enter the second number: ');

% Addition
add_result = num1 + num2;
fprintf('Addition of %.2f and %.2f is: %.2f\n', num1, num2, add_result);

% Subtraction
sub_result = num1 - num2;
fprintf('Subtraction (%.2f - %.2f) is: %.2f\n', num1, num2, sub_result);

% Multiplication
mul_result = num1 * num2;
fprintf('Multiplication of %.2f and %.2f is: %.2f\n', num1, num2, mul_result);




